class Comparison:
    def _eq(a, b):
        return a == b
    
    def _ne(a, b):
        return a != b
    
    def _lt(a, b):
        return a < b
    
    def _ge(a, b):
        return a >= b
    
    def _gt(a, b):
        return a > b

    def _le(a, b):
        return a <= b
    
    def _is(a, b):
        return a is b
    
    def _isnot(a, b):
        return a is not b